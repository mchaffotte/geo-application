CREATE TABLE question (id BIGINT NOT NULL, wording VARCHAR(255), image_id BINARY, quiz_id BIGINT, PRIMARY KEY(id));
CREATE TABLE quiz (id BIGINT NOT NULL, answer_type VARCHAR(2), PRIMARY KEY(id));
